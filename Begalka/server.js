function разобратьКарту() {
  const сетка = [];
  const точкиСпавна = [];
  const точкиВрагов = [];
  let дверь = null;

  for (let y = 0; y < КАРТА_ОСНОВА.length; y += 1) {
    const ряд = КАРТА_ОСНОВА[y].split("");
    for (let x = 0; x < ряд.length; x += 1) {
      const клетка = ряд[x];
      if (клетка === "S") {
        точкиСпавна.push({ x: x + 0.5, y: y + 0.5 });
        ряд[x] = ".";
      } else if (клетка === "E") {
        точкиВрагов.push({ x: x + 0.5, y: y + 0.5 });
        ряд[x] = ".";
      } else if (клетка === "D") {
        дверь = { x, y, progress: 0 };
        ряд[x] = ".";
      }
    }
    сетка.push(ряд);
  }

  if (!дверь) {
    throw new Error("На карте нет двери.");
  }
  if (точкиСпавна.length === 0) {
    точкиСпавна.push({ x: 1.5, y: 1.5 });
  }

  return {
    ширина: сетка[0].length,
    высота: сетка.length,
    сетка,
    точкиСпавна,
    точкиВрагов,
    дверь
  };
}

function случайныйКод() {
  const символы = "ABCDEFGHJKMNPQRSTUVWXYZ23456789";
  let код = "";
  for (let i = 0; i < 5; i += 1) {
    код += символы[Math.floor(Math.random() * символы.length)];
  }
  return код;
}

function создатьКодКомнаты() {
  let попытки = 0;
  while (попытки < 1000) {
    const код = случайныйКод();
    if (!комнаты.has(код)) {
      return код;
    }
    попытки += 1;
  }
  throw new Error("Не удалось создать код.");
}

function нормализоватьУгол(угол) {
  let next = угол;
  while (next > Math.PI) {
    next -= Math.PI * 2;
  }
  while (next < -Math.PI) {
    next += Math.PI * 2;
  }
  return next;
}

function ограничить(значение, минимум, максимум) {
  return Math.max(минимум, Math.min(максимум, значение));
}

function расстояние(ax, ay, bx, by) {
  return Math.hypot(ax - bx, ay - by);
}

function очиститьИмя(имя) {
  if (typeof имя !== "string") {
    return "Игрок";
  }
  const cleaned = имя.trim().replace(/[^\w \-]/g, "").slice(0, 18);
  return cleaned || "Игрок";
}

