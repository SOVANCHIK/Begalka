function выбратьСпавн(комната) {
  const кандидаты = [...комната.карта.точкиСпавна];
  if (кандидаты.length === 0) {
    return { x: 1.5, y: 1.5 };
  }
  for (let i = 0; i < 8; i += 1) {
    const выбор = кандидаты[Math.floor(Math.random() * кандидатов.length)];
    const тесно = [...комната.игроки.values()].some((игрок) => расстояние(выбор.x, выбор.y, игрок.x, игрок.y) < 1.1);
    if (!тесно) {
      return выбор;
    }
  }
  return кандидаты[Math.floor(Math.random() * кандидатов.length)];
}

