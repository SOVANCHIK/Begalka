function удалитьИгрока(сессия) {
  if (!сессия.кодКомнаты) {
    return;
  }
  const комната = комнаты.get(сессия.кодКомнаты);
  if (!комната) {
    сессия.кодКомнаты = null;
    return;
  }
  комната.игроки.delete(сессия.id);
  комната.активнаДо = Date.now();
  if (комната.победительId === сессия.id) {
    комната.победительId = null;
    комната.времяПобеды = 0;
  }
  if (комната.игроки.size === 0) {
    комнаты.delete(комната.код);
  } else {
    разослатьСостояние(комната, Date.now());
  }
  сессия.кодКомнаты = null;
}

function клеткаЗаблокирована(комната, tileX, tileY) {
  if (tileX < 0 || tileY < 0 || tileX >= комната.карта.ширина || tileY >= комната.карта.высота) {
    return true;
  }
  if (tileX === комната.карта.дверь.x && tileY === комната.карта.дверь.y && комната.карта.дверь.progress < 1) {
    return true;
  }
  return комната.карта.сетка[tileY][tileX] === "#";
}

function занято(комната, x, y, радиус) {
  const точки = [
    [x, y],
    [x + радиус, y],
    [x - радиус, y],
    [x, y + радиус],
    [x, y - радиус],
    [x + радиус, y + радиус],
    [x - радиус, y + радиус],
    [x + радиус, y - радиус],
    [x - радиус, y - радиус]
  ];
  return точки.some(([sx, sy]) => клеткаЗаблокирована(комната, Math.floor(sx), Math.floor(sy)));
}

function попробоватьДвижение(сущность, комната, шагX, шагY, радиус) {
  const nextX = сущность.x + шагX;
  if (!занято(комната, nextX, сущность.y, радиус)) {
    сущность.x = nextX;
  }

  const nextY = сущность.y + шагY;
  if (!занято(комната, сущность.x, nextY, радиус)) {
    сущность.y = nextY;
  }
}

function естьЛинияВидимости(комната, x1, y1, x2, y2) {
  const dx = x2 - x1;
  const dy = y2 - y1;
  const dist = Math.hypot(dx, dy);
  if (dist < 0.01) {
    return true;
  }
  const шаги = Math.ceil(dist / 0.05);
  for (let i = 1; i <= шаги; i += 1) {
    const t = i / шаги;
    const sx = x1 + dx * t;
    const sy = y1 + dy * t;
    if (клеткаЗаблокирована(комната, Math.floor(sx), Math.floor(sy))) {
      return false;
    }
  }
  return true;
}

function выстрелить(комната, игрок, сейчас) {
  if (!игрок.жив || комната.победительId) {
    return;
  }
  let ближайшийВраг = null;
  let ближайшееРасстояние = Infinity;
  for (const враг of комната.враги.values()) {
    if (!враг.жив) {
      continue;
    }
    const dx = враг.x - игрок.x;
    const dy = враг.y - игрок.y;
    const dist = Math.hypot(dx, dy);
    if (dist > ДАЛЬНОСТЬ_ВЫСТРЕЛА) {
      continue;
    }
    const уголЦели = Math.atan2(dy, dx);
    const дельта = нормализоватьУгол(уголЦели - игрок.угол);
    if (Math.abs(дельта) > РАЗБРОС_ВЫСТРЕЛА) {
      continue;
    }
    if (!естьЛинияВидимости(комната, игрок.x, игрок.y, враг.x, враг.y)) {
      continue;
    }
    if (dist < ближайшееРасстояние) {
      ближайшееРасстояние = dist;
      ближайшийВраг = враг;
    }
  }

  if (!ближайшийВраг) {
    return;
  }

  ближайшийВраг.hp = Math.max(0, ближайшийВраг.hp - УРОН_ОРУЖИЯ);
  ближайшийВраг.подсветкаДо = сейчас + 180;
  if (ближайшийВраг.hp <= 0) {
    ближайшийВраг.жив = false;
    игрок.убийства += 1;
  }
}

function обновитьИгроков(комната, dt, сейчас) {
  for (const игрок of комната.игроки.values()) {
    if (!игрок.жив) {
      if (сейчас >= игрок.возрождениеВ) {
        const спавн = выбратьСпавн(комната);
        игрок.x = спавн.x;
        игрок.y = спавн.y;
        игрок.угол = 0;
        игрок.hp = МАКС_ЗДОРОВЬЕ;
        игрок.жив = true;
      }
      continue;
    }

    игрок.угол = нормализоватьУгол(игрок.угол + игрок.поворотОжидание + игрок.ввод.поворот * СКОРОСТЬ_ПОВОРОТА * dt);
    игрок.поворотОжидание = 0;

    const вперед = ограничить(игрок.ввод.вперед, -1, 1);
    const вбок = ограничить(игрок.ввод.вбок, -1, 1);
    let шагX = Math.cos(игрок.угол) * вперед + Math.cos(игрок.угол + Math.PI * 0.5) * вбок;
    let шагY = Math.sin(игрок.угол) * вперед + Math.sin(игрок.угол + Math.PI * 0.5) * вбок;
    const длина = Math.hypot(шагX, шагY);
    if (длина > 1) {
      шагX /= длина;
      шагY /= длина;
    }
    попробоватьДвижение(игрок, комната, шагX * СКОРОСТЬ * dt, шагY * СКОРОСТЬ * dt, РАДИУС_ИГРОКА);

    if (игрок.ввод.огонь && сейчас - игрок.последнийВыстрелВ >= ПЕРЕЗАРЯДКА_МС) {
      игрок.последнийВыстрелВ = сейчас;
      выстрелить(комната, игрок, сейчас);
    }
  }
}

function найтиБлижайшегоИгрока(комната, x, y) {
  let лучший = null;
  let лучшееРасстояние = Infinity;
  for (const игрок of комната.игроки.values()) {
    if (!игрок.жив) {
      continue;
    }
    const dist = расстояние(игрок.x, игрок.y, x, y);
    if (dist < лучшееРасстояние) {
      лучший = игрок;
      лучшееРасстояние = dist;
    }
  }
  return { игрок: лучший, расстояние: лучшееРасстояние };
}

function обновитьВрагов(комната, dt, сейчас) {
  for (const враг of комната.враги.values()) {
    if (!враг.жив) {
      continue;
    }

    const { игрок: цель, расстояние: dist } = найтиБлижайшегоИгрока(комната, враг.x, враг.y);
    if (!цель || комната.победительId) {
      if (сейчас >= враг.блуждатьДо) {
        враг.уголБлуждания = Math.random() * Math.PI * 2;
        враг.блуждатьДо = сейчас + 1400 + Math.random() * 900;
      }
      попробоватьДвижение(
        враг,
        комната,
        Math.cos(враг.уголБлуждания) * СКОРОСТЬ_ВРАГА * 0.45 * dt,
        Math.sin(враг.уголБлуждания) * СКОРОСТЬ_ВРАГА * 0.45 * dt,
        РАДИУС_ВРАГА
      );
      continue;
    }

    const видитЦель = dist <= ДАЛЬНОСТЬ_АГРО && естьЛинияВидимости(комната, враг.x, враг.y, цель.x, цель.y);
    if (видитЦель) {
      const кX = (цель.x - враг.x) / Math.max(dist, 0.0001);
      const кY = (цель.y - враг.y) / Math.max(dist, 0.0001);
      if (dist > ДАЛЬНОСТЬ_АТАКИ) {
        попробоватьДвижение(враг, комната, кX * СКОРОСТЬ_ВРАГА * dt, кY * СКОРОСТЬ_ВРАГА * dt, РАДИУС_ВРАГА);
      } else if (сейчас >= враг.следующаяАтакаВ) {
        враг.следующаяАтакаВ = сейчас + КД_АТАКИ_МС;
        цель.hp = Math.max(0, цель.hp - УРОН_АТАКИ);
        цель.последнийУронВ = сейчас;
        if (цель.hp <= 0) {
          цель.жив = false;
          цель.смерти += 1;
          цель.возрождениеВ = сейчас + ВРЕМЯ_ВОЗРОЖДЕНИЯ_МС;
        }
      }
    } else {
      if (сейчас >= враг.блуждатьДо) {
        враг.уголБлуждания = Math.random() * Math.PI * 2;
        враг.блуждатьДо = сейчас + 1000 + Math.random() * 1500;
      }
      попробоватьДвижение(
        враг,
        комната,
        Math.cos(враг.уголБлуждания) * СКОРОСТЬ_ВРАГА * 0.35 * dt,
        Math.sin(враг.уголБлуждания) * СКОРОСТЬ_ВРАГА * 0.35 * dt,
        РАДИУС_ВРАГА
      );
    }
  }
}

function живыеВраги(комната) {
  let счет = 0;
  for (const враг of комната.враги.values()) {
    if (враг.жив) {
      счет += 1;
    }
  }
  return счет;
}

function сброситьРаунд(комната) {
  const новаяКарта = разобратьКарту();
  комната.карта = новаяКарта;
  комната.враги.clear();
  новаяКарта.точкиВрагов.forEach((спавн) => {
    const id = randomUUID();
    комната.враги.set(id, {
      id,
      x: спавн.x,
      y: спавн.y,
      hp: МАКС_ЗДОРОВЬЕ_ВРАГА,
      жив: true,
      следующаяАтакаВ: 0,
      подсветкаДо: 0,
      уголБлуждания: Math.random() * Math.PI * 2,
      блуждатьДо: 0
    });
  });
  комната.победительId = null;
  комната.времяПобеды = 0;

  for (const игрок of комната.игроки.values()) {
    const спавн = выбратьСпавн(комната);
    игрок.x = спавн.x;
    игрок.y = спавн.y;
    игрок.угол = 0;
    игрок.hp = МАКС_ЗДОРОВЬЕ;
    игрок.жив = true;
    игрок.возрождениеВ = 0;
    игрок.поворотОжидание = 0;
  }
}

function обновитьДверьИПобеду(комната, dt, сейчас) {
  const осталось = живыеВраги(комната);
  if (осталось === 0 && !комната.победительId) {
    комната.карта.дверь.progress = ограничить(комната.карта.дверь.progress + СКОРОСТЬ_ОТКРЫТИЯ * dt, 0, 1);
  } else if (осталось > 0) {
    комната.карта.дверь.progress = 0;
  }

  if (!комната.победительId && комната.карта.дверь.progress >= 1) {
    for (const игрок of комната.игроки.values()) {
      if (!игрок.жив) {
        continue;
      }
      const кДвери = расстояние(игрок.x, игрок.y, комната.карта.дверь.x + 0.5, комната.карта.дверь.y + 0.5);
      if (кДвери <= 0.65) {
        комната.победительId = игрок.id;
        комната.времяПобеды = сейчас;
        break;
      }
    }
  }

  if (комната.победительId && сейчас - комната.времяПобеды > ВРЕМЯ_ДО_СБРОСА_МС) {
    сброситьРаунд(комната);
  }
}

function шагКомнаты(комната, dt, сейчас) {
  обновитьИгроков(комната, dt, сейчас);
  обновитьВрагов(комната, dt, сейчас);
  обновитьДверьИПобеду(комната, dt, сейчас);
}

function разослатьСостояние(комната, сейчас) {
  const игроки = [...комната.игроки.values()].map((игрок) => ({
    id: игрок.id,
    имя: игрок.имя,
    x: игрок.x,
    y: игрок.y,
    угол: игрок.угол,
    hp: игрок.hp,
    maxHp: МАКС_ЗДОРОВЬЕ,
    жив: игрок.жив,
    убийства: игрок.убийства,
    смерти: игрок.смерти,
    последнийВыстрелВ: игрок.последнийВыстрелВ,
    последнийУронВ: игрок.последнийУронВ
  }));

  const враги = [...комната.враги.values()].map((враг) => ({
    id: враг.id,
    x: враг.x,
    y: враг.y,
    hp: враг.hp,
    maxHp: МАКС_ЗДОРОВЬЕ_ВРАГА,
    жив: враг.жив,
    подсветка: враг.подсветкаДо > сейчас ? (враг.подсветкаДо - сейчас) / 180 : 0
  }));

  const победитель = комната.победительId ? комната.игроки.get(комната.победительId) : null;
  const основа = {
    type: "state",
    кодКомнаты: комната.код,
    дверь: {
      x: комната.карта.дверь.x,
      y: комната.карта.дверь.y,
      прогресс: комната.карта.дверь.progress,
      открыта: комната.карта.дверь.progress >= 1
    },
    враговОсталось: живыеВраги(комната),
    победительId: комната.победительId,
    имяПобедителя: победитель ? победитель.имя : "",
    игроки,
    враги,
    времяСервера: сейчас
  };

  for (const игрок of комната.игроки.values()) {
    отправитьСообщение(игрок.ws, { ...основа, you: игрок.id });
  }
}

function создатьКомнатуОбработчик(ws, сессия, сообщение) {
  удалитьИгрока(сессия);
  const код = создатьКодКомнаты();
  const комната = создатьКомнату(код);
  комнаты.set(код, комната);
  const игрок = добавитьИгрока(комната, ws, сессия, сообщение.name);

  отправитьСообщение(ws, {
    type: "joined",
    кодКомнаты: код,
    playerId: игрок.id,
    карта: картаВДанные(комната)
  });
  разослатьСостояние(комната, Date.now());
}

function присоединитьсяККомнате(ws, сессия, сообщение) {
  const код = String(сообщение.roomCode || "").trim().toUpperCase();
  if (!код) {
    отправитьСообщение(ws, { type: "error", message: "Введите код комнаты." });
    return;
  }

  const комната = комнаты.get(код);
  if (!комната) {
    отправитьСообщение(ws, { type: "error", message: "Комната не найдена. Проверьте код." });
    return;
  }
  if (комната.игроки.size >= МАКС_ИГРОКОВ) {
    отправитьСообщение(ws, { type: "error", message: "Комната заполнена." });
    return;
  }

  удалитьИгрока(сессия);
  const игрок = добавитьИгрока(комната, ws, сессия, сообщение.name);
  отправитьСообщение(ws, {
    type: "joined",
    кодКомнаты: код,
    playerId: игрок.id,
    карта: картаВДанные(комната)
  });
  разослатьСостояние(комната, Date.now());
}

function обработчикВвода(сессия, сообщение) {
  if (!сессия.кодКомнаты) {
    return;
  }
  const комната = комнаты.get(сессия.кодКомнаты);
  if (!комната) {
    сессия.кодКомнаты = null;
    return;
  }
  const игрок = комната.игроки.get(сессия.id);
  if (!игрок) {
    return;
  }
  const ввод = сообщение.input || {};
  игрок.ввод.вперед = ограничить(Number(ввод.forward) || 0, -1, 1);
  игрок.ввод.вбок = ограничить(Number(ввод.strafe) || 0, -1, 1);
  игрок.ввод.поворот = ограничить(Number(ввод.turn) || 0, -1, 1);
  игрок.ввод.огонь = Boolean(ввод.fire);
  игрок.поворотОжидание += ограничить(Number(ввод.turnDelta) || 0, -0.6, 0.6);
  комната.активнаДо = Date.now();
}

вебСокеты.on("connection", (ws) => {
  const сессия = {
    id: randomUUID(),
    кодКомнаты: null,
    имя: "Игрок"
  };

  отправитьСообщение(ws, { type: "hello", id: сессия.id });

  ws.on("message", (raw) => {
    let сообщение;
    try {
      сообщение = JSON.parse(String(raw));
    } catch (_err) {
      отправитьСообщение(ws, { type: "error", message: "Неверный формат." });
      return;
    }

    if (!сообщение || typeof сообщение.type !== "string") {
      return;
    }

    if (сообщение.type === "create_room") {
      создатьКомнатуОбработчик(ws, сессия, сообщение);
    } else if (сообщение.type === "join_room") {
      присоединитьсяККомнате(ws, сессия, сообщение);
    } else if (сообщение.type === "input") {
      обработчикВвода(сессия, сообщение);
    } else if (сообщение.type === "leave_room") {
      удалитьИгрока(сессия);
    }
  });

  ws.on("close", () => {
    удалитьИгрока(сессия);
  });
});

